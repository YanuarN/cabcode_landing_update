function generateSkillsHTML(skills) {
  return Object.entries(skills).map(([skillName, percentage]) => `
    <div class="container mt-4">
      <h5>${skillName}</h5>
      <div class="progress">
        <div class="progress-bar bg-success" style="width:${percentage}%">${percentage}%</div>
      </div>
    </div>
  `).join('');
}

function generateSocialFooter(talent) {
  const socialType = talent.socialType || 'linkedin';
  const socialUrl = talent.linkedin || talent.socialMedia;
  const iconClass = socialType === 'instagram' ? 'bi-instagram' : 'bi-linkedin';
  
  return `
    <div class="social" font-size="20px">
      <a href="${socialUrl}" target="_blank"><i class="bi ${iconClass}" style="font-size: xx-large;"></i></a>
    </div>
  `;
}

function showProfile(talentId) {
  const talent = talentsData.find(t => t.id === talentId);
  
  if (!talent) {
    console.error('Talent not found:', talentId);
    return;
  }

  Swal.fire({
    html: `
      <img src="${talent.image}" class="mb-3" alt="${talent.name}" style="max-width: 400px; border:solid; border-radius: 10%; border-color: #f85f56;">
      <h2 style="color: #f85f56;">
        <b>${talent.name}</b>
      </h2>
      <p>
        <i class="bx bxs-quote-alt-left quote-icon-left"></i>
        <i>${talent.description}</i>
        <i class="bx bxs-quote-alt-right quote-icon-right"></i>
      </p>
      <hr>
      <h3 style="color: #f85f56;">Skills</h3>
      ${generateSkillsHTML(talent.skills)}
    `,
    showCloseButton: true,
    showConfirmButton: false,
    footer: generateSocialFooter(talent)
  });
}
